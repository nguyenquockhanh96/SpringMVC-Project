public class TTT {
    private String maso;
    private String name;
    private String photo;
    private String thanhtich;
    private String kyluat;
    private String diemtongket;

    public String getMaso() {
        return maso;
    }

    public void setMaso(String maso) {
        this.maso = maso;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getThanhtich() {
        return thanhtich;
    }

    public void setThanhtich(String thanhtich) {
        this.thanhtich = thanhtich;
    }

    public String getKyluat() {
        return kyluat;
    }

    public void setKyluat(String kyluat) {
        this.kyluat = kyluat;
    }

    public String getDiemtongket() {
        return diemtongket;
    }

    public void setDiemtongket(String diemtongket) {
        this.diemtongket = diemtongket;
    }

    
}
